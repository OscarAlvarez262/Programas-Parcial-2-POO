    private void btnSumarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumarActionPerformed
        
        if (txtNumero1.getText().equals("") || txtNumero2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Faltan datos", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            int A = 0, B = 0, R = 0;
            A = Integer.parseInt(txtNumero1.getText());
            B = Integer.parseInt(txtNumero2.getText());
            R = A + B;
            lblResultado.setText(Integer.toString(R));
            btnReiniciar.setEnabled(true);
        }
    }//GEN-LAST:event_btnSumarActionPerformed

